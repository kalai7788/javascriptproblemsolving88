const number = parseFloat(prompt("Enter a number: "));

if (number > 100) {
  console.log(" The number is greater than 100.");
} else if (number < 50) {
  console.log(" The number is less than 50.");
} else {
  console.log(" The number is between 50 and 100.");
}
